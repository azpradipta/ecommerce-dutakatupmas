export const generateMetadata = async ({
  params,
}: {
  params: Promise<{ id: string }>;
}) => {
  const { id } = await params;
  const product = products.find((p) => p.id === parseInt(id));
  if (!product) return { title: "Product Not Found" };
  return {
    title: product.name,
    description: product.description,
  };
};

